package ForLoops;

public class forLoop {
    public static void main(String[] args) {
        for (int i =99; i >= 1 ; i-=2) {
            System.out.print(" "+i);
            
        }
        System.out.println();

        System.out.println("--------------------------");

        for (char i = 65; i <90 ; i++) {
            System.out.print(" "+i);

        }




    }
}
